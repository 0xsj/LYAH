
// basic numeric types for scala
// Byte
// Short
// Int
// Long
// Float
// Double
// BigDecimal
//


object NumericTypes {
  val byteValue: Byte = 127
  val shortValue: Short = -32768
  val ushortValue: Int = 65535
  val intValue: Int = -2147483648
  val uintValue: Long = 4294967295L
  val longValue: Long = -9223372036854775808L
  val ulongValue: BigInt = BigInt("18446744073709551615")
  val floatValue: Float = 3.14
  val doubleValue: Double = 3.141592653589793
  val bigDecimalValue: BigDecimal = BigDecimal("123456.789")
}
